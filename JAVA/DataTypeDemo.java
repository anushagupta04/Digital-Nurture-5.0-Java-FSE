// 6. DataTypeDemo.java
class DataTypeDemo {
    public static void main(String[] args) {
        int age = 20;
        float price = 99.5f;
        double salary = 45000.75;
        char grade = 'A';
        boolean passed = true;

        System.out.println(age);
        System.out.println(price);
        System.out.println(salary);
        System.out.println(grade);
        System.out.println(passed);
    }
}