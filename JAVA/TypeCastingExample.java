// 7. TypeCastingExample.java
class TypeCastingExample {
    public static void main(String[] args) {
        double d = 25.75;
        int i = (int) d;

        int num = 10;
        double converted = (double) num;

        System.out.println("Double to int: " + i);
        System.out.println("Int to double: " + converted);
    }
}